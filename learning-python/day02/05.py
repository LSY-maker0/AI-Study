# 十进制
a=110
# 二进制
d=0b110
# 八进制
b=0o110
# 十六进制
c=0x110

print(a,b,c,d)

# bin --> binary --> 十进制转二进制
print(bin(47))
# oct --> octal --> 十进制转八进制
print(oct(47))
# hex --> hexadecimal --> 十进制转十六进制
print(hex(47))


# 浮点数 科学计数法
e=123e-5 # 123乘以10的-5次方
print(e)
